package forloop;

public class po45tone45 {
	public static void main(String[] args) {
        int start = 45;
        int end = -45;
        
        if (start > end) {
            for (int i = start; i >= end; i--) {
                System.out.println(i);
            }
        } else {
            for (int i = start; i <= end; i++) {
                System.out.println(i);
            }
        }
    }
}


 








